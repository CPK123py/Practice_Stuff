public class DataType{
    public static void main(String[] args){
        int a = 3;
        float b =3.0f;
        double c = 3.0d;
        char d ='c';
        boolean e = true;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        
    }
}